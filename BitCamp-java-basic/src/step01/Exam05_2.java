//정수 literal 2-자리수 표기
package step01;
public class Exam05_2{
    public static void main(String[] args){
        //숫자에 _를 사용하여 숫자의 자릿수를 표시할 수 있다
        //물론 위치는 작성자 마음이다.
        
        System.out.println(12312312); 
 
        System.out.println(1231_2312); 

        System.out.println(12_312_312);

       
        //2진수
        System.out.println(0b1010001);
        System.out.println(0b1_010_001);


    }
}
