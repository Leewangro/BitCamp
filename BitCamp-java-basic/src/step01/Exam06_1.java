//부동소숫점의 literal
package step01;
public class Exam06{
    public static void main(String[] args){
        System.out.println(3.14);
        //exponential로 표기하기
        //=>숫자e지수 도는 숫자E지수
        System.out.println(3.14);
        System.out.println(0.314e1);
        System.out.println(0.0314e2);
        System.out.println(0.00314e3);
        System.out.println(31.4e-1);
        //이 기호를 사용하면 소숫점의 위치를 조정할 수 있다.
        //소숫점의 위치가 움직인다고 해서 부동소수점이라고 한다.
    }
}
